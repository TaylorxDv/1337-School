#include "libft.h"

char	*ft_strdup(const char *src)
{
	char	*ptr;
	int		len;
	int		i;

	len = 0;
	while (src[len] != '\0')
		len++;
	ptr = (char *) malloc((len * sizeof(char)) + 1);
	if (ptr == NULL)
		return (NULL);
	i = 0;
	while (src[i] != '\0')
	{
		ptr[i] = src[i];
		i++;
	}
	ptr[i] = '\0';
	return (ptr);
}
