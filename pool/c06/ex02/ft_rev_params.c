#include<unistd.h>

int	main(int argc, char *argv[])
{
	int	j;

	while (argc > 1)
	{
		j = 0;
		argc --;
		while (argv[argc][j])
		{
			write(1, &argv[argc][j], 1);
			j++;
		}
		write(1, "\n", 1);
	}
}
